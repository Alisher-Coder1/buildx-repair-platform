from enum import Enum


class ErrorSeverity(str, Enum):
    BLOCKING = "BLOCKING"
    WARNING = "WARNING"
    INFO = "INFO"


class ErrorCode(str, Enum):
    ERR_REQUIRED_FIELD_MISSING = "ERR_REQUIRED_FIELD_MISSING"
    ERR_OUT_OF_RANGE = "ERR_OUT_OF_RANGE"
    ERR_INVALID_ENUM_VALUE = "ERR_INVALID_ENUM_VALUE"
    ERR_EXTRA_FIELD_FORBIDDEN = "ERR_EXTRA_FIELD_FORBIDDEN"
    ERR_UNSUPPORTED_COVERING = "ERR_UNSUPPORTED_COVERING"
    ERR_INVALID_QUANTITY = "ERR_INVALID_QUANTITY"
    ERR_NOT_FOUND = "ERR_NOT_FOUND"
    ERR_INTERNAL_CALCULATION_ERROR = "ERR_INTERNAL_CALCULATION_ERROR"
